package tryingtowork;

public class IntegerFloatDivision {

	public static void main(String[] args) {
		System.out.println(4 + 5);
		System.out.println(4 - 5);
		System.out.println(4 - 5 * 9);
		System.out.println(3 / 7);
		System.out.println(1 / 3);


		System.out.println();
		System.out.println(7.00 / 3);
		System.out.println(3.00 / 7);
		
		System.out.println(3 / 7.00);
		System.out.println(59 * 100 / 60.0);
		System.out.println();
		
		double y = 1 / 3;
		System.out.println(y);
		
		double x = 1.00 / 3.00;
		System.out.println(x);
		
	}

}
